#include <stdio.h>
/**
 * main - entry point
 *
 * Return: Always(0)
 */
int main(void)
{
	char b[] = {'z', 'y', 'x', 'w', 'v', 'u', 't', 's', 'r',
		'q', 'p', 'o', 'n', 'm', 'l', 'k', 'j', 'i', 'h',
		'g', 'f', 'e', 'd', 'c', 'b', 'a'};
	int r;

	for (r = 0; r <= b[r] ; r++)
	{
		putchar(b[r]);
	}
	putchar('\n');
	return (0);
}
