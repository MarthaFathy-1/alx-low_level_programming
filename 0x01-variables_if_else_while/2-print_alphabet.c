#include <stdio.h>
/**
 * main - entry point
 *
 * Return: return value always (0)
 */
int main(void)
{
	char lowercase[] = {'a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q',
	 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z'};
	int i;
	for (i=0; i <= lowercase[i]; i++)
	{
		putchar(lowercase[i]);
		putchar('\n');
	}
	return (0);
}
