#include <stdio.h>
/**
 * main - Entry point
 *
 * Return: return value
 */
int main(void)
{
	char lowercase[] = {'a', 'b', 'c', 'd', 'f', 'g',
		'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p',
		'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z'};
	int i;

	for (i = 0; i <= lowercase[i]; i++)
	{
		putchar(lowercase[i]);
	}
	putchar('\n');
	return (0);
}
